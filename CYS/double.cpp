/*
Double Data Type (double)
The double data type is used to store decimal numbers with higher precision. 
The keyword used to define double-precision floating-point numbers is double. 
Its size is 8 bytes (on 64-bit systems) and can store the values 
in the range from 1.7e-308 to 1.7e+308

Syntax

double name;

*/
#include <iostream>
using namespace std;

int main() {
  
      // double precision floating point variable
      double pi = 3.1415926535;
      cout << pi;
  
    return 0;
}
// Output: 3.14159
