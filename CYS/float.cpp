/*
Floating Point Data Type (float)
Floating-point data type is used to store numbers with decimal points. 
The keyword used to define floating-point numbers is float. 
Its size is 4 bytes (on 64-bit systems) and can store values in the 
range from 1.2E-38 to 3.4e+38.

Syntax

float name;
*/

#include <iostream>
using namespace std;

int main() {
  
      // Floating point variable with a decimal value
      float f = 36.5;
      cout << f;
  
    return 0;
}
// Output: 36.5